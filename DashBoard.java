      public void actionPerformed(ActionEvent e)
      {
          if (e.getSource() == sub) {
                  String data1;
                  String data
                      = "Name : "
                        + tname.getText() + "\n"
                        + "Mobile : "
                        + tmno.getText() + "\n";
                  if (male.isSelected())
                      data1 = "Gender : Male"
                              + "\n";
                  else
                      data1 = "Gender : Female"
                              + "\n";   
                  String data3 = "Address : " + tadd.getText();
                  tout.setText(data + data1 + data3);
                  tout.setEditable(false);
                  res.setText("Registration Successfully..");
          }
   
          else if (e.getSource() == reset) {
              String def = "";
              tname.setText(def);
              tadd.setText(def);
              tmno.setText(def);
              res.setText(def);
              tout.setText(def);
              resadd.setText(def);
          }
      }
    	
    	
    	
    	
    	
    	
